package entites;

/**
 * The Class Compo.
 */
public class Compo {

	/** The id art. */
	private int id_art;
	
	/** The id bon. */
	private int id_bon;
	
	/** The quantité. */
	private int quantité;
	
	/**
	 * Gets the id art.
	 *
	 * @return the id art
	 */
	public int getId_art() {
		return id_art;
	}
	
	/**
	 * Gets the id bon.
	 *
	 * @return the id bon
	 */
	public int getId_bon() {
		return id_bon;
	}
	
	/**
	 * Gets the quantité.
	 *
	 * @return the quantité
	 */
	public int getQuantité() {
		return quantité;
	}
	
	/**
	 * Sets the id art.
	 *
	 * @param id_art the new id art
	 */
	public void setId_art(int id_art) {
		this.id_art = id_art;
	}
	
	/**
	 * Sets the id bon.
	 *
	 * @param id_bon the new id bon
	 */
	public void setId_bon(int id_bon) {
		this.id_bon = id_bon;
	}
	
	/**
	 * Sets the quantité.
	 *
	 * @param quantité the new quantité
	 */
	public void setQuantité(int quantité) {
		this.quantité = quantité;
	}
}
